class Calculator:
    def __init__(self,num1,num2):
        self.a=num1
        self.b=num2


    
    def add(self):
        return (self.a+self.b)

    def substract(self):
        return (self.a-self.b)

    def multiply(self):
        return (self.a*self.b)

    def divide(self):
        return (self.a/self.b)